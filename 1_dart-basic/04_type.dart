void main() {
  // String
  String name = 'Dart';
  print(name);

  // int
  int age = -1 >>> 1;
  print(age);

  // double
  double pi = 3.14;
  print(pi);

  // bool
  bool isTrue = true;
  print(isTrue);

  // List
  List<String> names = ['Dart', 'Flutter', 'Java', 'Kotlin'];
  print(names);

  // Map
  Map<String, String> person = {'name': 'Dart', 'age': '25', 'city': 'Jakarta'};
  print(person);

  // var
  var hello = 'Hello, Dart!';
  print(hello);

  // dynamic
  dynamic dynamicVar = 'Hello, Dart!';
  print(dynamicVar);
}
